package oopddemo3;

public class Hostel 
{

	String name;
	String location;

	public Hostel(String name, String location) {
		this.name = name;
		this.location = location;
	}

	public String getName() {
		return name;
	}

	public String getLocation() {
		return location;
	}
	
	
	
	
}
